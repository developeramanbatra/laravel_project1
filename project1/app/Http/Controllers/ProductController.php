     */
    public function store(Request $request)
    {
        Product::create([
            'companyId'=>$request->companyId,
            'product_name'=>$request->product_name,
            'price'=>$request->price,
            'quantity'=>$request->quantity,
        ]);
        return back()->with('success','Product Added');
    }

